package main;

public class ForLoop {
	public static void main(String args[]) {
		/* A estrutura For serve para executar uma determinada rotina 
		 * diversas vezes. Diferentemente da estrutura While, esta estrutura 
		 * serve para realizar tarefas que tem quantidade determinada para 
		 * serem repetidas. */
		for(int contador = 0 ; contador <= 10 ; contador++) {
			System.out.println("** " + contador + " **");
		}
	}
}
