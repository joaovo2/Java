package methodNonAccessModifiers;

public class Modifiers {
	/* Métodos e atributos podem ser restritos por meios de 
	 * modificadores de não acesso.
	 * 
	 *  Métodos e atributos definidos como 'final' não podem 
	 *  ser sobrescritos ou modificados. 
	 *  
	 * Métodos e atributos definidos como 'static' são métodos que 
	 * pertencem a classe e não ao objeto. Estes métodos podem ser 
	 * usados chamando direto pelo nome da classe. 
	 * 
	 *  Métodos definidos como 'abstract' só podem ser usados em classes 
	 *  abstratas. Métodos abstratos não possuem corpo. O corpo do método 
	 *  é definido pela classe que herda a classe abstrata.
	 *  
	 *  Métodos definidos como 'transient', atributos e métodos são  
	 *  ignorados ao serializar o objeto que os contém. 
	 *  
	 * Métodos definidos como 'sinchronized' só podem ser acessados 
	 * por um thread por vez. 
	 * 
	 * Atributos definidos como 'volatile' não são armazenado em cache 
	 * localmente e é sempre lido na "memória principal".
​*/
}
